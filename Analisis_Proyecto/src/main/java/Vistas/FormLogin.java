    private void btnInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioActionPerformed
        FormMenu menu = new FormMenu();
        Timer timer = new Timer();
        
        intentos++;
        String usu = txtUsuario.getText();
        String contra = txtContraseña.getText();

        if (usu.equals("alex") && contra.equals("edu")) {
            this.dispose();
            menu.setVisible(true);
        } else {
            if (intentos == 3) {
               
                 btnInicio.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Ha superado el número máximo de intentos. El formulario se desbloqueará en 10 segundos.");
                
         TimerTask tarea = new TimerTask() {
            @Override
            public void run() {
                btnInicio.setEnabled(true); 
            }
        };
            
         timer.schedule(tarea, 10000);
         
         intentos=intentos-2;
            } else {
                JOptionPane.showMessageDialog(null, "El nombre de usuario o la contraseña son incorrectos.");
                txtUsuario.setText("");
                txtContraseña.setText("");
            }
        }


    }//GEN-LAST:event_btnInicioActionPerformed

 
